    public int maximumCount(int[] nums) {
        int leftPtr = 0, rightPtr = nums.length - 1;
        int pos = -1;
        while (leftPtr <= rightPtr) {
            int midPtr = leftPtr + (rightPtr - leftPtr) / 2;
            if (nums[midPtr] <= 0) {
                leftPtr = midPtr + 1;
            } else if (midPtr > 0 && nums[midPtr - 1] > 0) {
                rightPtr = midPtr - 1;
            } else {
                pos = midPtr;
                break;
            }
        }
        if (pos >= 0) {
            int neg = pos - 1;
            while (neg >= 0 && nums[neg] == 0) {
                neg--;
            }
            return Math.max(neg + 1, nums.length - pos);
        }
        int neg = nums.length - 1;
        while (neg >= 0 && nums[neg] == 0) {
            neg--;
        }
        return neg + 1;
    }
